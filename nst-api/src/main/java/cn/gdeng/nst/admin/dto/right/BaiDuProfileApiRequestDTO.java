package cn.gdeng.nst.admin.dto.right;

import java.io.Serializable;

/**
 * @author yangjj
 */
public class BaiDuProfileApiRequestDTO implements Serializable{
    
    private static final long serialVersionUID = 5281468482596436097L;
    //服务名
    private String serviceName;
    //方法名 
    private String methodName;
   
    public BaiDuProfileApiRequestDTO() {
    }   

    public BaiDuProfileApiRequestDTO(String serviceName, String methodName) {
        super();
        this.serviceName = serviceName;
        this.methodName = methodName;
    }

    public String getServiceName() {
        return serviceName;
    }
  
    public void setServiceName(String serviceName) {
        this.serviceName = serviceName;
    }
  
    public String getMethodName() {
        return methodName;
    }
    
    public void setMethodName(String methodName) {
        this.methodName = methodName;
    }   
}
